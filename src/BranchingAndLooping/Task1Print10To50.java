package BranchingAndLooping;

public class Task1Print10To50 {
    public static void main(String[] args) {


        //Printing numbers from 10 to 50 using for loop
        for (int i = 10; i <=50 ; i++) {
            System.out.println("i value is from "+ i );

        }

        //Printing numbers from 10 to 50 using while loop

        int i =10;
        while (i<=50){
            System.out.println("Print i value "+ i);
            i++;
        }
    }
}
